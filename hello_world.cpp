#include <iostream> //header file library
using namespace std; //means that we can use names of objects and variables from standard library
// if you dont want to include namespace you can write std:: for some standard library objects
int main() {
	cout << "Hello World!" << endl; // endl is another (\n) way to insert a new line
	int age = 3; // data types in c++ are int, float, double, char, string, bool
	cout << "I am "<< age << " years old.";
	return 0;
}
